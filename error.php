<h3 id="section4">Бизнес-уведомления</h3>
<p>
  Бизнес-уведомления — это информационные сообщения о текущих статусах бизнес-процессов, требующие внимания пользователя для продолжения рабочих операций. Они не являются ошибками, а отражают нормальное функционирование системы и необходимость принятия решений в рамках бизнес-логики.<br>
</p>
<p>Пример 1:  <b>Заявка №... требует согласования</b><br>
  Когда возникает:
</p>
<p>
  <ul>
      <li>Заявка создана и направлена на утверждение (например, согласование финансовых операций, доступа к системе)</li>
      <li>Требуется подпись ответственного лица для продолжения процесса</li>
  </ul>
</p>
<p>Требуемые действия - обратить внимание и исправить:</p>
<p>
  <ul>
      <li>Проверить данные заявки в соответствующем разделе, нажать «Согласовать» или «Отклонить»</li>
  </ul>
</p>
<p>Пример 2:  <b>Вагон №... простой 2 дня</b><br>
  Когда возникает:
</p>
<p>
  <ul>
      <li>Вагон находится в простое дольше установленного срока (например, из-за задержки погрузки)</li>
  </ul>
</p>
<p>Требуемые действия - обратить внимание и исправить:</p>
<p>
  <ul>
      <li>Принять решение - срочно загрузить вагон или перенести срок</li>
  </ul>
</p>

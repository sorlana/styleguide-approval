<h3 id="section1">Системные ошибки</h3>
<p>
  Системные ошибки — это непредвиденные сбои в работе приложения, которые не обрабатываются специальными сценариями и возникают из-за внешних или внутренних факторов, не связанных с пользовательским вводом.<br>
</p>
<p>Пример 1:  <b>Ошибка «Неправильный логин и пароль»</b><br>
  Когда возникает:
</p>
<p>
  <ul>
      <li>Пользователь ввел неверные учетные данные при входе в систему</li>
      <li>Превышено количество попыток входа (макс. 3 раза)</li>
  </ul>
</p>
<p>Требуемые действия - обратить внимание и исправить:</p>
<p>
  <ul>
      <li>Проверить раскладку клавиатуры (кириллица/латиница, Caps Lock)</li>
      <li>Ввести данные заново (макс. 3 попытки)</li>
      <li>Обратиться в поддержку для восстановления пароля</li>
  </ul>
</p> 
<img class="im mgb" src="./img/error/er1.jpg" />
<p>Пример 2:  <b>Ошибка «Снижение производительности»</b><br>
  Когда возникает:
</p>
<p>
  <ul>
      <li>Сервер обрабатывает запрос дольше 5 секунд (но не таймаут)</li>
      <li>Медленная загрузка данных из-за высокой нагрузки на систему</li>
  </ul>
</p>
<p>Требуемые действия - обратить внимание и исправить:</p>
<p>
  <ul>
      <li>Дождаться завершения операции (макс. 10 секунд)</li>
      <li>Не повторять нажатия (риск дублирования действий)</li>
      <li>Если таймер истек → сообщить в поддержку с указанием времени ошибки</li>
  </ul>
</p> 
<img class="im mgb" src="./img/error/er2.jpg" />
<p>Пример 3:  <b>Ошибка загрузки данных в таблице</b><br>
  Когда возникает:
</p>
<p>
  <ul>
      <li>Таблица не может загрузить данные из-за сбоя API, разрыва соединения или временной недоступности сервера</li>
  </ul>
</p>
<p>Требуемые действия - обратить внимание / исправить:</p>
<p>
  <ul>
      <li>Проверить подключение к интернету (Wi-Fi, мобильная сеть)</li>
      <li>Дождаться окончания таймера и нажать «Обновить» — система автоматически повторит запрос</li>
  </ul>
</p>

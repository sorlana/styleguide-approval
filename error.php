<h3 id="section2">Ошибки валидации</h3>
<p>
  Ошибки валидации — это ожидаемые сбои при обработке данных, которые обрабатываются штатными сценариями и возникают из-за некорректного, неполного или несоответствующего формату пользовательского ввода/загрузки данных.<br>
</p>
<p>Пример 1:  <b>Ошибки валидации формы</b><br>
  Когда возникает:
</p>
<p>
  <ul>
      <li>Пользователь отправляет форму с некорректными или неполными данными (пустое обязательное поле, неправильный формат email, пароль короче 8 символов)</li>
  </ul>
</p>
<p>Требуемые действия - обратить внимание и исправить:</p>
<p>
  <ul>
      <li>Исправить данные в полях с ошибками</li>
      <li>Убедиться, что все сообщения в правой панели исчезли перед повторной отправкой формы</li>
  </ul>
</p> 
<img
  class="im mgb"
  src="./img/error/er3.jpg"
/>
<p>Пример 2:  <b>Ошибки загрузки файлов</b><br>
  Когда возникает:
</p>
<p>
  <ul>
      <li>Пользователь пытается загрузить файл с недопустимым форматом (например, .exe вместо .pdf)</li>
      <li>Размер файла превышает лимит (макс. например 10 МБ)</li>
      <li>Сервер не принял файл из-за технических ограничений (обрыв соединения во время загрузки)</li>
  </ul>
</p>
<p>Требуемые действия - обратить внимание и исправить:</p>
<p>
  <ul>
      <li>Проверить требования к файлу (формат, размер, тип) — информация в подсказке под полем загрузки</li>
      <li>Исправить файл (сжать изображение, конвертировать формат)</li>
  </ul>
</p> 

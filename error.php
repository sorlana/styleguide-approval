<h3 id="section5">Бизнес-ошибки</h3>
<p>
  Бизнес-ошибки — это ошибки, формируемые системой при обработке запроса, которые отражают несоответствие данным бизнес-правилам (например, дублирование данных, нарушение логики процесса). Они не являются техническими сбоями, а показывают, что запрос не может быть выполнен из-за контекста бизнес-логики.<br>
</p>
<p>Пример:  <b>Клиент заблокирован</b><br>
  Когда возникает:
</p>
<p>
  <ul>
      <li>При попытке выполнить операцию с заблокированным клиентом (перевод средств, оформление заказа)</li>
  </ul>
</p>
<p>Требуемые действия - обратить внимание и исправить:</p>
<p>
  <ul>
      <li>Нажать «Обратиться в поддержку» для ускорения разблокировки</li>
  </ul>
</p>

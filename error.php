<h3 id="section3">Серверные ошибки и предупреждения</h3>
<p>
  Серверные ошибки — это критичные сбои в работе серверной инфраструктуры, возникающие из-за внутренних проблем приложения (падение сервера, сбои БД, перегрузка API) и не зависящие от действий пользователя.<br>
</p>
<p>Пример 1:  <b>Ошибка 500</b><br>
  Когда возникает:
</p>
<p>
  <ul>
      <li>Сервер не может обработать запрос из-за внутренней ошибки (баг в коде, падение БД)</li>
  </ul>
</p>
<p>Требуемые действия - связаться с поддержкой или зайти на сайт позже:</p>
<p>
  <ul>
      <li>Не пытаться повторить действие — ошибка не решится перезагрузкой</li>
      <li>Связаться с поддержкой, указав время возникновения ошибки</li>
  </ul>
</p>
<img class="im mgb" src="./img/error/er6.jpg" />
<p>Пример 2:  <b>Передупреждение о технических работах</b><br>
  Когда возникает:
</p>
<p>
  <ul>
      <li>Плановое обслуживание серверов (обновление ПО, масштабирование инфраструктуры)</li>
  </ul>
</p>
<p>Требуемые действия - зайти на сайт позже:</p>
<p>
  <ul>
      <li>Не отправлять данные повторно — система временно недоступна для операций</li>
      <li>Дождаться окончания работ</li>
  </ul>
</p>
